
# The AND logic function
print("********** AND **********")
a, b = False, False
print("F AND F -- ", a and b)
a, b = False, True
print("F AND T -- ", a and b)
a, b = True, False
print("T AND F -- ", a and b)
a, b = True, True
print("T AND T -- ", a and b)

# The OR logic function
print("********** OR **********")
a, b = False, False
print("F AND F -- ", a or b)
a, b = False, True
print("F AND T -- ", a or b)
a, b = True, False
print("T AND F -- ", a or b)
a, b = True, True
print("T AND T -- ", a or b)



